#include "ctm_libs.h"

char	*ft_strrchr(char *s, int n)
{
	char	*c;

	c = 0;
	while (*s)
	{
		if (*s == n)
			c = s;
		s++;
	}
	if (n == '\0')
		return (s);
	s = c;
	return (s);
}
