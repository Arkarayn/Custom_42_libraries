#include "ctm_libs.h"

int	ft_strncmp(char *s1, char *s2, unsigned int n)
{
	while (n > 0 && *s1 != '\0' && *s1 == *s2)
	{
		s1++;
		s2++;
		n--;
	}
	return ((*(unsigned char *)s1 - *(unsigned char *)s2) * n);
}
