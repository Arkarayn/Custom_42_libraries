#include "ctm_libs.h"

void	ft_putnbr_fd(int n, int fd)
{
	int	b;

	if (n == -2147483648)
	{
		write(fd, "-2147483648", 11);
		return ;
	}
	if (n < 0)
	{
		n = -n;
		write(fd, "-", 1);
	}
	if (n < 10)
	{
		b = n + 48;
		write(fd, &b, 1);
	}
	else
	{
		b = n % 10 + 48;
		ft_putnbr_fd(n /= 10, fd);
		write(fd, &b, 1);
	}
}
