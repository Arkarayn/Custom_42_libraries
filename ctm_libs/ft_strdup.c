#include "ctm_libs.h"

char	*ft_strdup(char *s)
{
	int		i;
	char	*c;

	i = 0;
	while (s[i] != '\0')
		i++;
	c = malloc(i * sizeof(char) + 1);
	if (c != NULL)
	{
		i = 0;
		while (s[i] != '\0')
		{
			c[i] = s[i];
			i++;
		}
	}
	c[i] = '\0';
	return (c);
}
